// Ejercicio 1: En este ejercicio, se define una función utilizando function declaration que recibe dos
// números, los suma y devuelve el resultado. Se realiza la llamada a la función y se muestra
// el resultado en la consola.

function suma(numero1, numero2) {
    return numero1 + numero2;
}

let resultado = suma(10, 20);

console.log(`El resultado de la suma es: ${resultado}`);